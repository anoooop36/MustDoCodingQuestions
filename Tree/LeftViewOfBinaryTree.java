class CommonLevel {
    int val = 0;
}

class Tree {
    /*
     * idea is to print first leftmost node of each level. It can be done by level
     * order traversal or preorder and by having a commonLevel across recursion and
     * by maintaining a variale for current level.
     */
    void leftViewUtil(Node root, CommonLevel commonLevel, int currentLevel) {
        if (root == null)
            return;
        if (currentLevel == commonLevel.val) {
            System.out.print(root.data + " ");
            commonLevel.val++;
        }
        leftViewUtil(root.left, commonLevel, currentLevel + 1);
        leftViewUtil(root.right, commonLevel, currentLevel + 1);
    }

    void leftView(Node root) {
        leftViewUtil(root, new CommonLevel(), 0);
    }
}